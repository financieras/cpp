#include <iostream>

int main() {
    int num1, num2, suma;

    // Solicitar al usuario los dos números
    std::cout << "Ingrese el primer número: ";
    std::cin >> num1;

    std::cout << "Ingrese el segundo número: ";
    std::cin >> num2;

    // Sumar los dos números
    suma = num1 + num2;

    // Mostrar el resultado
    std::cout << "La suma de los dos números es: " << suma << std::endl;

    return 0;
}

